x = input("type the first integer : ")
y = input("type the second integer : ")

if x == y:
    print(str(x) + "==" + str(y))

if x != y:
    print(str(x) + "!=" + str(y))

if x < y:
    print(str(x) + "<" + str(y))

if x <= y:
    print(str(x) + "<=" + str(y))

if x > y:
    print(str(x) + ">" + str(y))

if x >= y:
    print(str(x) + ">=" + str(y))